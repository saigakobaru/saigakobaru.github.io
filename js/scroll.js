// JavaScript Document$


$(function() {
  $('#move_target').click(function() {
    // スクロール
    $('html,body').animate({scrollTop: 0}, 800, 'swing');
  });
});



$(function() {
  $('#move').click(function() {
    // スクロール
    $('html,body').animate({scrollTop: 2500}, 800, 'swing');
  });
});

